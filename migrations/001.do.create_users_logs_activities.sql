CREATE TABLE user (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    full_name TEXT NOT NULL,
    user_name TEXT NOT NULL UNIQUE,
    email TEXT NOT NULL,
    password TEXT NOT NULL
);

CREATE TABLE activity (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    assigned_user INTEGER REFERENCES user(id) ON DELETE CASCADE NOT NULL,
    activity_name TEXT NOT NULL
);

CREATE TABLE mood_log (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    assigned_user INTEGER REFERENCES user(id) ON DELETE CASCADE NOT NULL,
    assigned_activity INTEGER REFERENCES activity(id) ON DELETE CASCADE NOT NULL,
    start_mood INTEGER NOT NULL,
    end_mood INTEGER NOT NULL,
    notes TEXT,
    date_created TIMESTAMPZ NOT NULL DEFAULT now()
);